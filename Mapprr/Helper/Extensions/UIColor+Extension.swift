import UIKit

extension UIColor {

    public class var navigationColor: UIColor {
        return UIColor(red: 0, green: 0, blue: 0, alpha: 1)
    }

    public class var navigationTitleColor: UIColor {
        return UIColor(red: 1, green: 1, blue: 1, alpha: 1)
    }

    public class var themeGreenColor: UIColor {
        return UIColor(red: 58/255, green: 191/255, blue: 117/255, alpha: 1)
    }

    public class var themeBlueColor: UIColor {
        return UIColor(red: 36/255, green: 126/255, blue: 210/255, alpha: 1)
    }

    public class var headerTextBlueColor: UIColor {
        return UIColor(red: 40/255, green: 138/255, blue: 202/255, alpha: 1)
    }

    public class var themeTextColor: UIColor {
        return UIColor(red: 1, green: 1, blue: 1, alpha: 1)
    }

    public class var transBackgroundColor: UIColor {
        return UIColor(red: 0, green: 0, blue: 0, alpha: 0.1)
    }

    public class var lineColor: UIColor {
        return UIColor(red: 224/256, green: 224/256, blue: 224/256, alpha: 1)
    }

    public class var cancelGreyColor: UIColor {
        return UIColor(red: 90/256, green: 98/256, blue: 104/256, alpha: 1)
    }

    public class var textColor: UIColor {
        return UIColor(red: 0, green: 0, blue: 0, alpha: 1)
    }

    public class var placeholderGrayColor: UIColor {
        return UIColor(red: 153/255, green: 153/255, blue: 153/255, alpha: 1)
    }
    
    open class var transColor:UIColor {
        return UIColor(red: 0, green: 0, blue: 0, alpha: 0.5)
    }

    open class var tableBackgroundColor:UIColor {
        return UIColor(red: 239/256, green: 239/256, blue: 244/256, alpha: 1)
    }

}
